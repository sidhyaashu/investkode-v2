class CompanyMaster(Base):
    __tablename__ = "company_master"
    __table_args__ = {"schema": "public"}

    fincode = Column(Integer, primary_key=True, autoincrement=False)

    # BSE fields
    scripcode = Column(Integer)
    scrip_name = Column(String(35))
    scrip_group = Column(String(10))
    bse_scrip_id = Column(String(100))
    bse_sublisting = Column(String(50))

    # Company fields
    compname = Column(String(255))
    s_name = Column(String(100))
    industry = Column(String(100))
    isin = Column(String(20))
    status = Column(String(20))

    # NSE fields
    symbol = Column(String(20), index=True)
    series = Column(String(10))
    securitytoken = Column(Integer)
    nse_sublisting = Column(String(50))

    # Optional metadata
    ind_code = Column(Integer)
    hse_code = Column(Integer)
    house = Column(String(50))
    rformat = Column(String(5))
    fformat = Column(String(5))
    fv = Column(Float)
    flag = Column(String(1))
    
    # Relationships
    equity = relationship("CompanyEquity", back_populates="company", primaryjoin="CompanyMaster.fincode == foreign(CompanyEquity.fincode)", uselist=True)


